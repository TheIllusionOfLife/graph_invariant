def build_wikidata_materials_kg() -> KnowledgeGraph:
    """Build a medium-scale materials science knowledge graph (~250 entities, ~900 edges).

    Modelled after Wikidata's materials science subgraph with relations
    mapped to the shared 7-type vocabulary.
    """
    kg = KnowledgeGraph(domain="wikidata_materials")
    for entity_id, entity_type, props in _ENTITIES:
        kg.add_entity(Entity(id=entity_id, entity_type=entity_type, properties=props))
    for source, target, edge_type in _EDGES:
        kg.add_edge(TypedEdge(source=source, target=target, edge_type=edge_type))
    return kg
